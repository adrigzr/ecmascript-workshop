'use strict';

var Funnel = require('broccoli-funnel');
var path = require('path');

module.exports = {

	files: [
		'babel-standalone/babel.min.js'
	],

	toTree: function() {
		return new Funnel('node_modules', {
			include: this.files,
			getDestinationPath: function(relativePath) {
				return 'assets/node_modules/' + relativePath;
			}
		});
	}

};
